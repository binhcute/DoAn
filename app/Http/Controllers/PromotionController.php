        $khuyenmai->promotion_percent = $request->promotion_percent;
        $khuyenmai->promotion_money = $request->promotion_money;
        $khuyenmai->end_at = $request->end_at;
        $khuyenmai->save();
        return response()->json(
            [
                'status' => 'success',
                'message' => 'Thanh Cong'

            ],200);
    }

